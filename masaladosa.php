Recipe type: breakfast
Cuisine: south indian
Serves: 15 Dosas
Ingredients
�	1 cup urad dal
�	3 cup sona masuri rice / dosa rice
�	� tsp methi / fenugreek seeds
�	1 cup paper avalakki / beaten rice / thin poha
�	oil for roasting dosa
�	salt to taste
Instructions
1.	soak urad dal and rice, methi in a separate bowl. add in enough water and soak for 5 hours.
2.	first drain the water from rice and blend to smooth batter. add water if required to make a smooth paste.
3.	transfer to a large vessel. and keep aside
4.	now blend urad dal to smooth fluffy batter.
5.	now add a cup of paper avalakki to the blender. and grind to a smooth thick paste.
6.	transfer the batter into the same bowl and mix well.
7.	cover and ferment in a warm place for 12 hours.
8.	the next day you can see the batter has raised well indicating its well fermented.
9.	mix the batter slightly.
10.	take required batter in a different bowl. and add salt to the batter and mix well.
11.	heat a non-stick tava / griddle and pour a ladleful of the batter on it and spread it in a circular motion thin circle.
12.	smear a little oil over it.
13.	cover the dosa for a minute and cook till the bottom of dosa turns golden brown.
14.	fold over to make a semi-circle or a roll and serve immediately with coconut chutney and sambar.
