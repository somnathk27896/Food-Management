Recipe type: Main, Vegetarian
Cuisine: Indian, North Indian
Serves: 3-4

Ingredients (used american measuring cup, 1 cup = 250 ml)

1.5 tbsp butter or oil
� tsp cumin seed
�th tsp carom seed
250 gm button mushroom
180 gm onion chopped finely
200 gm tomato finely chopped
100 gm capsicum finely chopped
2 tbsp coriander leaves + 1 tbsp more for garnish
1 tbsp ginger-garlic paste
2 tsp pav bhaji masala
1 tsp garam masala
a pinch of turmeric powder
1 tsp red chili powder or as required
1 tsp sugar
1 tbsp chat masala or 1tsp amchur powder
black salt or salt as per taste

How to make the recipe?

Firstly heat the tawa on high heat. Once it's hot just add chopped capsicum.
Keep stirring add few drops of water and heat them for a minute more.
Remove them and place aside.
In the same pan add butter or oil add ajwain seed and cumin seed.
Once they crackle add they chopped onion and coriander leaves.
Add the ginger garlic paste and saute.
The onion should change color to pale pink or light brown.
Add the capsicum again and saute.
Once capsicum softens add the tomatoes.
Saute them add all the spice powder, salt except chat masala or amchur powder. Also, add sugar if using.
Cook until tomatoes soften adda little water if moisture dries.
Once all veggies soften mash with a potato masher.
Allow the mixture to dry.
Time to add mushrooms.
Keep stirring the mushrooms will leave water and cook in their own juice. Mushrooms cook very fast once they change their color and texture that means they are cooked.
You could make this as semi gravy or dry it's up to you. Lastly, add crushed kasuri methi leaves, coriander leaves and chat masala.
Serve your mushroom tawa masala recipe with roti, phulkas or toasted bread.
 

