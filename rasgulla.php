how to make the recipe:
preparing the chenna:
1.	take milk in a pan and keep it to boil on a low to medium flame. i used organic cow milk which does not have much fat. if you use buffalo milk, it has too much of fat and makes a thick layer of malai/cream floating on top. you have to remove the thick layer of malai, before you proceed with the making of chenna.
2.	so when the milk is heating up, line a strainer or bowl with a cheese cloth or muslin.
3.	keep on stirring the milk at intervals so that the froth is not formed on top and the base does not get browned or the milk solids get stuck to the bottom.
4.	when the milk comes to a boil, then reduce the flame to its lowest. add 1 to 3 tbsp lemon juice. first add 1 tbsp lemon juice and stir. if the milk has not curdled completely, then add 1 tbsp more. keep the lemon juice handy with you. depending on the quality of milk, you may need to add 1 to 3 tbsp of lemon juice. stir after adding the lemon juice. sometimes i have added 1 tbsp lemon juice and the milk has curdled and sometimes i needed to add 3 tbsp too. vinegar also can be added instead of lemon juice. another option is curd/yogurt. add 4 to 5 tbsp of yogurt or more if required. with yogurt you don't need to rinse the chenna in water later.
5.	as soon as the milk curdles, switch off the flame. the milk should curdle completely with the green watery whey. if the milk does not curdle, then add � to 1 tbsp of lemon juice more.
6.	now pour the curdled milk in the cheese cloth/muslin lined strainer or bowl.
7.	gather the muslin from the sides and rinse the chenna or coagulated milk very well in running water. this brings down the temperature of chenna as well as removes the lemony flavor and tangy taste from the chenna.
8.	now squeeze the muslin with your hands very well, so that excess water is drained from the chenna. remember there should not be excess water or moisture in the chenna as then the rasgullas will break when cooking.
9.	place a heavy weight on the chenna for 7 to 8 minutes. you can also hang the chenna for about 30 minutes.
preparing the rasgulla balls:
1.	after 7 to 8 minutes, remove the cheesecloth from the chenna. note that the chenna should not have too much moisture nor be too dry.
2.	add 1 tsp sooji or rava or semolina. you can also add all purpose flour/maida. adding either of them helps to bind the mixture. for a gluten free option, add corn starch instead of semolina or all purpose flour.
3.	first mix the sooji with chenna and then begin to knead.
4.	with the heels of your palms mash the chenna and knead. keep on collecting the chenna from the sides and continue to mash and knead. this kneading process is very important and also decides the texture of chenna. when you feel your palms becoming a bit greasy, its time to stop. just a bit of greasiness is required. avoid kneading to an extent where the whole chenna becomes greasy. i kneaded for about 10 minutes as i have very light hands. so depending on the quality of chenna and the pressure you apply while kneading, you can take more or less time. note that the chenna should just begin to get greasy.
5.	knead to a smooth ball of chenna.
6.	now pinch small portions from the chenna and roll them between your palms to a smooth round ball.
7.	prepare all small balls this way. cover all the chenna balls with a moist muslin or kitchen towel and keep aside.
preparing sugar syrup for rasgulla:
1.	in a large pan, take 2 cups sugar and 4 cups water. you have to use a large pot or pan so that there is enough space for the rasgullas to cook and increase in size. you can also use a pressure cooker. i used a pan measuring 8.5 inches in diameter and 4.5 in height. a 3.4 litre stainless steel pan.
2.	keep this pan on stove top and heat the sugar solution. stir so that the sugar dissolves. i cooked kneaded the chenna and cooked the sugar syrup simultaneously. you can also do this way.
3.	add 1 tbsp milk and stir. adding milk helps in removing impurities. if there are no impurities then you don't need to add milk and directly proceed to step 25
4.	once the sugar solution becomes hot, the impurities begin to float on the top. you can either remove it with a spoon. or strain the impurities in a cheese cloth/muslin lined strainer.
5.	now from the purified sugar solution, reserve � cup in a cup or mug. this � cup of sugar solution we will be adding to the cooking rasgullas.
6.	reserve another 1 cup of the sugar solution in a serving bowl.
cooking rasgullas:
1.	the rest of the 2.5 cups of sugar solution you add it back to the large pan and bring it to a boil on a medium high flame.
2.	slid the rasgullas gently into the sugar solution.
3.	once all the rasgullas have been added to the sugar solution, shake the pan. don't stir the rasgullas with a spoon. just gently shake the pan.
4.	cover immediately with a lid and let the rasgullas cook. keep the flame to a medium or medium high.
5.	after 4 minutes, open the lid and add � cup of the reserved sugar solution. shake the pan. adding this reserved sugar solution ensures that the temperature & consistency of the sugar solution is maintained and the sugar does not cook to its thread consistencies.
6.	cover again and continue to cook.
7.	after 4 minutes, again add � cup of the reserved sugar solution. cover and again cook for 2 minutes. i cooked for 10 minutes. the timing will vary depending on the thickness & quality of pan, the depth of the pan and flame intensity.
to check the doneness of rasgullas:
1.	there are two ways. first place the rasgulla in a bowl or cup of water. if the rasgulla sinks to the bottom its cooked. second method is to press a small portion of the rasgulla with your finger. if the pressed portion bounces back to its original shape, its cooked
2.	once the rasgullas are cooked, switch off the flame and keep the pan down.
3.	now take each rasgulla with a spoon and place it in the bowl containing the 1 cup of sugar solution. cover and keep aside.
4.	let the sugar solution in which the rasgullas were cooked, become warm. then add this to the serving bowl containing the rasgullas.
5.	once the whole mixture has cooled down, add 1 to 2 tbsp rose water. if you don't have rose water you can also kewra water (pandanus extract) or � tsp cardamom powder. stir gently and allow the rasgullas to be soaked in the sugar syrup for 30 minutes.
6.	you can serve the rasgullas now or refrigerate them and serve later.
notes
1. leftover syrup can be used to make nimbu pani (lemonade). you can also add it to shikanjvi or fruit juices or while making cakes.


