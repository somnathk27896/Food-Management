Introduction:
Kanda Poha makes an easy and super quick breakfast dish. The trick to make best Kande Pohe is in selecting and soaking the poha in right way. Watch out the video clip for better understanding about Poha soaking process.
Ingredients:
�	Peanuts,roasted 1/4 cup
�	Cashewnuts,roasted 10-12
�	Almonds,roasted 10-12
�	Raisins 1/2 tablespoon
�	Pressed rice (poha) 2 cups
�	Oil 2 tablespoons
�	Mustard seeds 1/2 teaspoon
�	Cumin seeds 1 teaspoon
�	Asafoetida a pinch
�	Green chillies,slit 2
�	Curry leaves 10-12
�	Onion ,finely chopped 1 medium
�	Potato,peeled, sliced thickly 1 medium
�	Carrot,grated 1/2 cup
�	Turmeric powder 1/4 teaspoon
�	Salt to taste
�	Sugar a pinch
�	Fresh coriander leaves,chopped 2 tablespoons

<center> Method </center>
1.Wash pressed rice under running water, drain excess water and allow to stand for ten to fifteen minutes. Heat oil in a non-stick pan, add mustard seeds and once they splutter, add cumin seeds and saute for half a minute.

2.Add asafoetida, green chillies, curry leaves, onion and saute till the onion is golden brown. Add potato and carrot and mix. Sprinkle little water, cover and cook till potatoes are cooked.

3.Add pressed rice, turmeric powder, peanuts, cashewnuts, almonds, raisins, salt and sugar and toss well. Cover and cook for another five to seven minutes. Serve hot garnished with coriander leaves.

