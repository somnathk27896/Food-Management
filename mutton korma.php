<pre>Total Time: 1 1/2 hrs - 2 hrs + 3 hrs Marination
Serves: 2 - 3


Ingredients:

750 gms Mutton
3/4 cup Fried Onions (store bought / home made)
1 1/2 tblsp Coriander Powder
1 1/2 tsp Red Chilly Powder (For heat)
1 1/2 tblsp Kashmiri Red Chilly Powder (For Color)
2 tblsp Garlic Paste
2 tblsp Ginger Paste
8 tblsp Curd
3 tblsp Ghee / Oil
2 Cups water
Few Kewra Essence Drop
Salt To Taste
Whole Spices: 
2 Bay Leaf
2 Black Cardamom
5 - 6 Green Cardamom
7 Cloves
2" Cinnamom Stick
For Garnish
1 tblsp Ginger Juliennes

How to make Mutton Korma :

If you do not have store bought fried Onions then follow these instructions to make them at home.
For Crisp Fried Onions - Finely slice the onions and heat the oil. Just before frying the onions sprinkle 1 tsp of cornflour on the sliced onions and very quickly put them in oil for frying on medium flame. Keep turning them and make sure they do not get dark brown.
Once they reach the golden brown color, take them out with a slotted spoon and spread them on a plate to dry and get crisp. Do not take them out a paper towel or they will get soggy. Also keep the color of the fried onions 1 shade lighter as they get dark once crisp.
In a big bowl mix together beaten curd, coriander powder, red chilly powder, kashmiri red chilly powder, red chilly powder, garlic - ginger paste and 1/2 cup fried onions.
Now add the mutton pieces and mix well so that all the mutton pieces are coated well with the marinade. Cover the bowl and let it marinate in the fridge for couple of hours. If short of time then even 1/2 - 1 hour is good enough.
Befor starting to cook, bring the marinated mutton to room temperature and add the salt.
Now take a heavy bottom kadai / pan and heat the ghee / oil in it. Add all the whole spices - Bay Leaf, Black Cardamom, Green Cardamom, Cloves and Cinnamom Stick.
When the whole spices begin to sputter add the marinated mutton and let the mutton fry on high flame till all the moisture is evaporated and all the pieces are evenly browned. This usually takes 10 minutes. Getting the perfect brown color is very important. So fry accordingly.
Lower the flame and add the water. Cover the pan / kadai and let the mutton cook for 40 - 45 minutes
Now add the remaining fried onions and kewra drops and mix well.
Cover the pan and further cook for 20 - 30 minutes or till the mutton is soft and fork tender.
In between check for the consistency of the gravy and add water accordingly.
Garnish with ginger juliennes.
Serve hot with Naan / Tandoori Roti / Roomali Roti.

